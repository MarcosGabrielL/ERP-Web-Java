package Bean;

/**
 *
 * @author Marcos
 */
public class Fornecedor {
    
    private String nome;
    private String email;
    private String empresa;
    private String produtos;
    private String telefone1;
    private String telefone2;
    private String endereçoempresa;

    /**
     * @return the nome
     */
    public String getNome() {
        return nome;
    }

    /**
     * @param nome the nome to set
     */
    public void setNome(String nome) {
        this.nome = nome;
    }

    /**
     * @return the email
     */
    public String getEmail() {
        return email;
    }

    /**
     * @param email the email to set
     */
    public void setEmail(String email) {
        this.email = email;
    }

    /**
     * @return the empresa
     */
    public String getEmpresa() {
        return empresa;
    }

    /**
     * @param empresa the empresa to set
     */
    public void setEmpresa(String empresa) {
        this.empresa = empresa;
    }

    /**
     * @return the produtos
     */
    public String getProdutos() {
        return produtos;
    }

    /**
     * @param produtos the produtos to set
     */
    public void setProdutos(String produtos) {
        this.produtos = produtos;
    }

    /**
     * @return the telefone1
     */
    public String getTelefone1() {
        return telefone1;
    }

    /**
     * @param telefone1 the telefone1 to set
     */
    public void setTelefone1(String telefone1) {
        this.telefone1 = telefone1;
    }

    /**
     * @return the telefone2
     */
    public String getTelefone2() {
        return telefone2;
    }

    /**
     * @param telefone2 the telefone2 to set
     */
    public void setTelefone2(String telefone2) {
        this.telefone2 = telefone2;
    }

    /**
     * @return the endereçoempresa
     */
    public String getEndereçoempresa() {
        return endereçoempresa;
    }

    /**
     * @param endereçoempresa the endereçoempresa to set
     */
    public void setEndereçoempresa(String endereçoempresa) {
        this.endereçoempresa = endereçoempresa;
    }
            
    
}
